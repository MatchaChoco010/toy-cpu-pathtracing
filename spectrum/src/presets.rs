/// CIEの定義した標準光源のF光源に対応するスペクトルを返す関数。
/// 蛍光灯に対応する。
#[inline(always)]
pub fn cie_f<const INDEX: u8>() -> Spectrum {
    assert!(INDEX <= 12);
    match INDEX {
        1 => CACHED_SPECTRUM.cie_illum_f1.clone(),
        2 => CACHED_SPECTRUM.cie_illum_f2.clone(),
        3 => CACHED_SPECTRUM.cie_illum_f3.clone(),
        4 => CACHED_SPECTRUM.cie_illum_f4.clone(),
        5 => CACHED_SPECTRUM.cie_illum_f5.clone(),
        6 => CACHED_SPECTRUM.cie_illum_f6.clone(),
        7 => CACHED_SPECTRUM.cie_illum_f7.clone(),
        8 => CACHED_SPECTRUM.cie_illum_f8.clone(),
        9 => CACHED_SPECTRUM.cie_illum_f9.clone(),
        10 => CACHED_SPECTRUM.cie_illum_f10.clone(),
        11 => CACHED_SPECTRUM.cie_illum_f11.clone(),
        12 => CACHED_SPECTRUM.cie_illum_f12.clone(),
        _ => unreachable!(),
    }
}

